    sudo apt remove -y "$package"
    if [ $? -eq 0 ]; then
        echo "‚úÖ $package removed successfully!"
    else
        echo "‚ùå Failed to remove $package."
    fi
}

purge_package() {
    local package="$1"
    echo
    echo "üì¶ Purging package: $package"
    sudo apt purge -y "$package"
    if [ $? -eq 0 ]; then
        echo "‚úÖ $package purged successfully!"
    else
        echo "‚ùå Failed to purge $package."
    fi
}

search_package() {
    local term="$1"
    echo "üîç Searching for: $term"
    apt search "$term"
}


full_upgrade() {
    echo "‚öô  Running full upgrade...!"
    apt_update && \
    apt_upgrade && \
    apt_autorm && \
    apt_autocls && \
    if [ $? -eq 0 ]; then
	echo "‚úÖ Full upgrade completed successfully!"
    else
	echo "‚ùå An error occurred during the upgrade. Exit code: $?"
    fi
}

main() {
    show_banner
    det_release
    echo

	# Parse command-line flags
	case "$1" in
	    -aud|--apt-update)
		apt_update
		;;
	    -aur|--system-upgrade)
		apt_upgrade
		;;
	    -arm|--apt-autoremove)
		apt_autorm
		;;
	    -acl|--apt-autoclean)
		apt_autocls
		;;
	    -fu|--full-upgrade)
		full_upgrade
		;;
	    -dur|--dist-upgrade)
		dist_upgrade
		;;
	    -i|--install)
		shift
		if [ $# -eq 0 ]; then
		    echo "‚ùå No packages specified to install."
		    exit 1
		fi

		for package in "$@"; do
		    install_package "$package"
		done
		;;
	    -rmp|--remove)
		shift
		if [ $# -eq 0 ]; then
		    echo "‚ùå No packages specified to remove."
                    exit 1
		fi

		for package in "$@"; do
		    remove_package "$package"
		done
		;;
	    -pp|--purge)
                shift
                if [ $# -eq 0 ]; then
                    echo "‚ùå No packages specified to purge."
                    exit 1
                fi

                for package in "$@"; do
                    purge_package "$package"
                done
                ;;
	    -ps|--search)
		shift
		if [ $# -eq 0 ]; then
		    echo "‚ùå No packages specified to search."
		    exit 1
		fi

		for term in "$@"; do
		    search_package "$term"
		done
		;;
	    -h|--help)
		echo
		echo "Usage: robohelp [option]"
		echo "	-aud, --apt-update	[1] Update APT Repositories"
		echo "	-aur, --apt-upgrade 	[1] Upgrade installed packages"
		echo "	-arm, --apt-autoremove  [1] Remove unnecessary packages"
		echo "	-acl, --apt-autoclean	[1] Clean up local repository"
		echo "	-fu,  --full-upgrade	Run full system upgrade with options from [1]"
		echo
		echo "	-dur, --dist-upgrade	Run distribution update for system"
		echo
		echo "	-i,   --install <name>	Install package via apt"
		echo "	-ps,  --search 	<name>	Search package in repository"
		echo " 	-rmp, --remove  <name>	Remove package from system"
		echo "	-pp,  --purge	<name>	Remove package with all its dependencies"
		echo
		echo "	-h,   --help		Show this help message"
		;;
	    *)
		echo
		echo "Unknown or no flag provided. Try -h for help."
		;;
	esac
}

# Call the main function
main "$@"
